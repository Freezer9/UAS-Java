    private void showMenu(){
        for(int i = 0; i < menuAmount; i++){
            listMenu[i] = new ListMenu();
            listMenu[i].setMenuDisplay(i);
            jPanel1.add(listMenu[i]);
            listMenu[i].setVisible(true);
        }
    }
    
